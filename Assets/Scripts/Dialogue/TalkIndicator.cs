using UnityEngine;

public class TalkIndicator : MonoBehaviour
{

    public static TalkIndicator Instance;

    void Awake()
    {
        Instance = this;
        gameObject.SetActive(false);
    }

    public void Show()
    {
        gameObject.SetActive(true);
    }

    public void Hide()
    {
        gameObject.SetActive(false);
    }
}
